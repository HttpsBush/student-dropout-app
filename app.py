age = st.number_input("Age at Enrollment", 17, 70, 20)
admission_grade = st.number_input("University Admission Score", 0.0, 200.0, 120.0)
previous_grade = st.number_input("Previous Academic Score", 0.0, 200.0, 120.0)

tuition = st.selectbox("Tuition Fees Status", ["Yes", "No"])

